// Function to get random questions
function getRandomQuestions(count = 10, excludeIds = []) {
    const availableQuestions = questionsDatabase.filter(q => !excludeIds.includes(q.id));
    const shuffled = availableQuestions.sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count);
}

// Function to get questions by category
function getQuestionsByCategory(category, count = 10) {
    const categoryQuestions = questionsDatabase.filter(q => q.category === category);
    const shuffled = categoryQuestions.sort(() => Math.random() - 0.5);
    return shuffled.slice(0, count);
}

// Function to get all categories
function getAllCategories() {
    return [...new Set(questionsDatabase.map(q => q.category))];
}

